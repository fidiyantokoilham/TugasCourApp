    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        dataCustomers.setTanggal(new Date());
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MMMM-yyyy");
        String nowfat = sdf.format(dataCustomers.getTanggal());
        
        DecimalFormat df = new DecimalFormat("###,###.##");
        pengirimLabel.setText(dataCustomers.getNamaPengirim());
        noTelponPengirim.setText(dataCustomers.getNoTelponPengirim());
        jalanPengirim.setText(dataCustomers.getNamaJalanPengirim());

        penerima2.setText(dataCustomers.getNamaPenerima());
        jalan2.setText(dataCustomers.getNamaJalanPenerima());
        kota2.setText(dataCustomers.getKotaPenerima());
        provinsi2.setText(dataCustomers.getProvinsiPenerima());
        telpon2.setText(dataCustomers.getNoelponPenerima());
        tanggalTextField.setText(nowfat);
        tanggal2.setText(nowfat);
        

        tabelPane.setVisible(true);
        if (iyaRadio.isSelected()) {
            double hargaBarang = Double.parseDouble(hargaTextField.getText());

            pengiriman.setHargaAsuransi(pengirimanDao.hitungHargaArusansi(hargaBarang));
            biayaAsuransi.setText(df.format(pengiriman.getHargaAsuransi()));
            pengiriman.setTotalHargaWithAsuransi(pengiriman.getTotalHarga() + pengiriman.getHargaAsuransi());
            biayaPengirimanLabel1.setText(df.format(pengiriman.getTotalHarga()));
            totalBiaya.setText(df.format(pengiriman.getTotalHargaWithAsuransi()));

        } else {
            pengiriman.setHargaAsuransi(0);
            biayaAsuransi.setText("-");
            biayaPengirimanLabel1.setText(df.format(pengiriman.getTotalHarga()));
            totalBiaya.setText(df.format(pengiriman.getTotalHarga()));
        }
        finalButton.setEnabled(true);
    }//GEN-LAST:event_jButton3ActionPerformed

